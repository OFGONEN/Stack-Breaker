using UnityEngine;

namespace FFStudio
{
    [ CreateAssetMenu( fileName = "event_", menuName = "FF/Event/ReferenceGameEvent" ) ]
    public class ReferenceGameEvent : GameEvent
    {
        public object eventValue;

        public void Raise( Component value )
        {
			eventValue = value;
			Raise();
		}
    }
}